package curso.api.rest.model;

public class UserReport {
	
	private String salarioInicio;
	
	private String salarioFim;
	
	
	

	public String getSalarioInicio() {
		return salarioInicio;
	}

	public void setSalarioInicio(String salarioInicio) {
		this.salarioInicio = salarioInicio;
	}

	public String getSalarioFim() {
		return salarioFim;
	}

	public void setSalarioFim(String salarioFim) {
		this.salarioFim = salarioFim;
	}

	
	
	
}
